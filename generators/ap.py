"""Phase 2C — Accounts Payable: vendors + non-payroll opex invoices + payments.

For each (period × cost_center × non-payroll account), generate AP invoices
that aggregate to a target opex amount. Targets are derived from:

    target_opex_period_cc = revenue_period × opex_pct × cost_center_share
    non_payroll_target    = target_opex × NON_PAYROLL_OPEX_SHARE_BY_ROLLUP[rollup]

Vendors are pre-generated by category and matched to the expense type.

This module does NOT generate payroll AP invoices — those are in payroll.py
because they share enough domain logic (FTE counts, semi-monthly cadence, SBC
accruals) to warrant a separate file.
"""
from __future__ import annotations

import uuid
from collections import defaultdict
from dataclasses import dataclass
from datetime import date, datetime, time, timedelta, timezone

import numpy as np
from dateutil.relativedelta import relativedelta
from faker import Faker

from . import config as cfg
from .types import (
    APInvoiceRow,
    APPaymentRow,
    ChartOfAccountsRow,
    CostCenterRow,
    VendorRow,
)


# =============================================================================
# Calibration knobs
# =============================================================================

# Share of each opex rollup that flows through AP (vs payroll).
# Salaries & SBC are excluded; these are the residual opex categories.
NON_PAYROLL_OPEX_SHARE: dict[str, float] = {
    "cogs": 0.55,    # hosting, cloud bills, third-party services
    "sm":   0.45,    # marketing programs (digital, events, branding), T&E, tooling
    "rd":   0.30,    # cloud infra, software/tooling, contractors
    "ga":   0.50,    # legal, audit, insurance, office, software
}

# Within each rollup × cost-center function, which non-payroll accounts get
# allocated and at what relative weight. Drives which accounts get hit by AP.
# Account names match generators/entities.py exactly.
NON_PAYROLL_ACCOUNTS_BY_ROLLUP_FN: dict[tuple[str, str], dict[str, float]] = {
    # COGS — only allocated to customer_success and IT cost-centers
    ("cogs", "customer_success"): {
        "Hosting & Infrastructure": 0.55,
        "Customer Success Cost":    0.20,
        "Professional Services Cost": 0.25,
    },
    ("cogs", "it"): {
        "Hosting & Infrastructure": 0.85,
        "Customer Success Cost":    0.15,
    },
    # S&M — sales has T&E + tooling; marketing has programs + tooling
    ("sm", "sales"): {
        "Sales Travel and Entertainment": 0.55,
        "Sales Tooling":                  0.45,
    },
    ("sm", "marketing"): {
        "Marketing Programs - Digital":  0.40,
        "Marketing Programs - Events":   0.30,
        "Marketing Programs - Branding": 0.20,
        "Marketing Tooling":             0.10,
    },
    # R&D — engineering and IT spend on cloud + tooling + contractors
    ("rd", "rd"): {
        "Cloud Infrastructure - R&D":  0.40,
        "R&D Software and Tooling":    0.25,
        "R&D Contractors":             0.35,
    },
    # G&A — legal/professional, audit, office, insurance, software
    ("ga", "ga"): {
        "Legal and Professional Fees": 0.30,
        "Audit and Tax Fees":          0.10,
        "Office and Facilities":       0.20,
        "Insurance":                   0.15,
        "G&A Software and Tooling":    0.25,
    },
}

# Vendor categories per cost-center function (pool of vendors a CC's invoices
# pick from).
VENDOR_CATEGORIES_BY_FUNCTION: dict[str, list[str]] = {
    "sales":            ["saas_tool", "office"],
    "marketing":        ["marketing_agency", "saas_tool"],
    "rd":               ["cloud_infra", "saas_tool", "contractor"],
    "customer_success": ["saas_tool", "contractor"],
    "ga":               ["legal", "saas_tool", "office"],
    "it":               ["cloud_infra", "saas_tool"],
}

# Number of vendors per category to pre-generate.
VENDORS_PER_CATEGORY: dict[str, int] = {
    "cloud_infra":      6,
    "marketing_agency": 12,
    "contractor":       20,
    "saas_tool":        25,
    "legal":            8,
    "office":           10,
    "payroll_provider": 1,
}

# Invoices per (period × cost_center × account) — random within range
INVOICES_PER_CELL_RANGE = (1, 4)

# AP receipts (payments): mostly within payment_terms days. Assume NET30 default.
DEFAULT_PAYMENT_TERMS = "NET30"
PAYMENT_TERMS_DAYS = {"NET30": 30, "NET45": 45, "NET60": 60}

# Share of AP invoices that pay early/late
AP_PAYMENT_BEHAVIOR = {
    "on_time": 0.75,   # within ±5d of due_date
    "early":   0.10,   # before due_date - 5d
    "late":    0.13,   # 5-30d late
    "open":    0.02,   # still open at FY25 EOP (some payment timing variation)
}

# AP cutoff for payment generation
AP_PAYMENT_CUTOFF = cfg.fy_end(2025) + timedelta(days=14)


# =============================================================================
# Public artifacts type
# =============================================================================

@dataclass(slots=True)
class APArtifacts:
    vendors: list[VendorRow]                # all vendors (incl. payroll_provider)
    ap_invoices: list[APInvoiceRow]
    ap_payments: list[APPaymentRow]


# =============================================================================
# Helpers
# =============================================================================

def _to_dt(d: date) -> datetime:
    return datetime.combine(d, time.min, tzinfo=timezone.utc)


def _round_money(x: float) -> float:
    return round(x, 2)


def _weighted_choice(rng: np.random.Generator, choices, weights):
    total = sum(weights)
    probs = [w / total for w in weights]
    return rng.choice(choices, p=probs)


def _periods_in_window(years: list[int]):
    """Yield calendar months in fiscal-year window (Feb of (year-1) → Jan of year)."""
    start = cfg.fy_start(min(years))
    end = cfg.fy_end(max(years))
    cursor = start.replace(day=1)
    while cursor <= end:
        yield cursor, cfg.period_yyyymm(cursor)
        cursor = cursor + relativedelta(months=1)


